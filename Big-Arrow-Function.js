//  Array functon

// Arrow Functions হলো একটি ES6 (ECMAScript 2015) ফিচার যা একটি সংক্ষিপ্ত সিনট্যাক্সে ফাংশন লেখার সুবিধা প্রদান করে। এটি নতুন একটি ফাংশন লেখার পদ্ধতি যা লেখা হয়:

const add = (a, b) => a + b;
console.log(add(5, 3)); // Output: 8

// উপরের উদাহরণে, (a, b) => a + b হলো Arrow Function, যা a এবং b দুইটি প্যারামিটার গ্রহণ করে এবং তাদের যোগফল রিটার্ন করে। এখানে আমরা সংক্ষেপে ফাংশনের বডির সাথে কোনো ব্রেস ({}) ব্যবহার করা না দেখতে পাচ্ছি, কারণ একটি একক স্টেটমেন্ট এখানে রয়েছে যা স্বয়ংক্রিয়ভাবে রিটার্ন হয়।

// Big array function ::

// Big Arrow Function (Fat Arrow Function):
// "Big Arrow Function" বা "Fat Arrow Function" হলো Arrow Functions-এর একটি অন্য নাম। যেমন:
const multiply = (a, b) => {
    return a * b;
};
console.log(multiply(5, 3)); // Output: 15

// উপরের উদাহরণে, আমরা দেখতে পাচ্ছি যে আমরা ফাংশনের বডির ভিতরে একটি ব্রেস ({}) ব্যবহার করেছি। এটি মূলত একটি মাল্টি-লাইন ফাংশন ডিফিনিশন এবং ফাংশনের বডির সমস্ত স্টেটমেন্ট গুলি একটি ব্লকে রাখা হয়েছে।

// তাই, Fat Arrow Function হলো Arrow Function এর একটি ধরণ, যেখানে ফাংশনের বডি একটি ব্লকে থাকে এবং এক্সপ্লিসিট রিটার্ন স্টেটমেন্ট ব্যবহার করা হয়।
