// *** what is Default paramiter in JavaScript:

// JavaScript এর Default Parameters হলো একটি ফাংশন ডিফিনেশনের অংশ, যা ফাংশনের প্যারামিটারের ডিফল্ট মান সেট করে। এটি ব্যবহার করে যদি ফাংশন কল করা হয় তখন প্যারামিটারে মান পাওয়া না যায় তবে ফাংশন স্বয়ংক্রিয়ভাবে সেই ডিফল্ট মানটি ব্যবহার করবে।

// এটি একটি উদাহরণে দেখা যাক:
function greet(name, greeting = 'Hello') {
    console.log(`${greeting}, ${name}!`);
}

greet('John'); // Output: Hello, John!
greet('Jane', 'Hi'); // Output: Hi, Jane!

// উদাহরণে, greet ফাংশনে আমরা name এবং greeting দুইটি প্যারামিটার পাস করেছি। তবে, আমরা দ্বিতীয় প্যারামিটার greeting এর ডিফল্ট মান সেট করেছি "Hello" হিসেবে। এখানে, যদি ফাংশন কল করা হয় এবং কোনো ডিফল্ট মান সরবরাহ না করা হয়, তবে ফাংশন স্বয়ংক্রিয়ভাবে সেই ডিফল্ট মান ব্যবহার করে।

// উপরের উদাহরণে, যখন আমরা greet('John') কল করলাম, তখন name প্যারামিটারের মান পাওয়া গেলো "John" এবং greeting প্যারামিটারের মান পাওয়া গেলো না, তাই ফাংশনে ডিফল্ট মান "Hello" ব্যবহার করে মেসেজ তৈরি হয়েছে।